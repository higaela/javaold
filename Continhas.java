public class Continhas{
     public static void main(String args[]){
        int a,b,c;
        a=10;
        b=7;
        
        c = a + b;
        System.out.println("a+b=" + c);
        
        c  =a - b;
        System.out.println("a-b=" + c);

        c  =a / b;
        System.out.println("a/b=int " + c);

        double d;
        d= a / b;
        System.out.println("a/b=double " + d);

        float e;
        e = a / b;
        System.out.println("a/b=float " + e);
     }
}